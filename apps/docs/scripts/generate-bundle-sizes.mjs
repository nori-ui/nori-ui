// Measures the per-component bundle cost of importing one nori-ui export at
// a time. For each named export of @nori-ui/core we synthesize a one-line
// entry, run esbuild with tree-shaking + minify, and record the gzipped
// payload. Output: apps/docs/components/bundle-sizes.generated.ts.
//
// Externals (react, react-native, nativewind, etc.) are excluded so the
// number reflects what nori-ui *adds* to the consumer's existing bundle —
// not the universe of dependencies they already ship.
//
// Run via the predev/prebuild package.json hooks alongside the other
// generators.

import { mkdtempSync, readdirSync, readFileSync, rmSync, writeFileSync } from 'node:fs';
import { tmpdir } from 'node:os';
import { dirname, join, relative } from 'node:path';
import { fileURLToPath } from 'node:url';
import { gzipSync } from 'node:zlib';
import { build } from 'esbuild';

const HERE = dirname(fileURLToPath(import.meta.url));
const DOCS_ROOT = join(HERE, '..');
const REPO_ROOT = join(HERE, '..', '..', '..');
const OUT_FILE = join(HERE, '..', 'components', 'bundle-sizes.generated.ts');

// Source of truth for the target list is the docs site itself: every
// component MDX page declares the component to measure with its
// <BundleSize component="X" /> tag (always near the top of the file).
// Walking the MDX directory means adding a docs page automatically
// pulls the component into the size table — no hand-curated list to
// drift out of sync.
const DOCS_COMPONENTS_DIR = join(REPO_ROOT, 'apps', 'docs', 'content', 'docs', 'components');
const BUNDLE_SIZE_TAG = /<BundleSize\s+component=["']([^"']+)["']\s*\/>/;

const TARGETS = (() => {
    const seen = new Set();
    for (const entry of readdirSync(DOCS_COMPONENTS_DIR, { withFileTypes: true })) {
        if (!entry.isFile() || !entry.name.endsWith('.mdx')) {
            continue;
        }
        const src = readFileSync(join(DOCS_COMPONENTS_DIR, entry.name), 'utf8');
        const match = src.match(BUNDLE_SIZE_TAG);
        if (!match) {
            throw new Error(
                `apps/docs/content/docs/components/${entry.name} is missing the <BundleSize component="…" /> tag. ` +
                    `Every component docs page must declare which component to measure.`
            );
        }
        seen.add(match[1]);
    }
    return [...seen].sort();
})();

// Treat real consumer-side dependencies as externals. These are loaded
// regardless of nori-ui usage; counting them would inflate every number
// and obscure what the library itself contributes. The list mirrors the
// `external` array in packages/core/tsup.config.ts plus the optional /
// platform-only peers consumers are expected to install themselves.
const EXTERNALS = [
    'react',
    'react-dom',
    'react-native',
    'react-native-web',
    'react-native-reanimated',
    'react-native-safe-area-context',
    'react-native-svg',
    'nativewind',
    'react-native-css-interop',
    'sonner',
    'sonner-native',
    'expo-blur',
    '@internationalized/date',
];

const tmpDir = mkdtempSync(join(tmpdir(), 'nori-bundle-'));

const measure = async (name) => {
    const entry = join(tmpDir, `${name}.entry.js`);
    writeFileSync(entry, `export { ${name} } from '@nori-ui/core';\n`);
    const result = await build({
        entryPoints: [entry],
        bundle: true,
        minify: true,
        treeShaking: true,
        format: 'esm',
        platform: 'browser',
        target: 'es2022',
        external: EXTERNALS,
        write: false,
        // Resolve bare specifiers (`@nori-ui/core`) against the docs
        // workspace's node_modules tree, not /tmp where the entry lives.
        nodePaths: [join(DOCS_ROOT, 'node_modules'), join(REPO_ROOT, 'node_modules')],
        absWorkingDir: REPO_ROOT,
        logLevel: 'silent',
    });
    const out = result.outputFiles[0];
    if (!out) {
        throw new Error(`esbuild produced no output for ${name}`);
    }
    const raw = out.contents.byteLength;
    const gzipped = gzipSync(out.contents).byteLength;
    return { raw, gzipped };
};

const sizes = {};
for (const name of TARGETS) {
    sizes[name] = await measure(name);
}

rmSync(tmpDir, { recursive: true, force: true });

const banner = `// AUTO-GENERATED by apps/docs/scripts/generate-bundle-sizes.mjs.
// Do not edit by hand. Re-run via the workspace predev/prebuild hooks.

`;

const body = `export type BundleSize = {
    /** Raw minified bytes. */
    raw: number;
    /** Gzipped bytes — the realistic "what the user pays" number. */
    gzipped: number;
};

export const bundleSizes = ${JSON.stringify(sizes, null, 4)} satisfies Record<string, BundleSize>;

export type BundleSizeName = keyof typeof bundleSizes;
`;

writeFileSync(OUT_FILE, banner + body);

console.log(`Generated ${relative(process.cwd(), OUT_FILE)} with ${Object.keys(sizes).length} components.`);
