// Reads every demo under apps/docs/components/demos/*.tsx and writes a
// generated `preview-sources.generated.ts` map of `<demo-name>` → verbatim
// source string. The Preview component imports from this map and shows it in
// the "Code" tab.
//
// Why not `?raw` query imports: Next.js's swc-loader still transforms .tsx
// files even with a `resourceQuery` rule, so `?raw` ends up containing the
// transpiled HMR-wrapped output instead of the file the human wrote. A
// pre-build codegen sidesteps the entire loader pipeline and works under
// both webpack and turbopack identically.
//
// Run via the `predev` / `prebuild` package.json hooks; no manual invocation
// is required during normal development.

import { readdirSync, readFileSync, writeFileSync } from 'node:fs';
import { dirname, join, relative } from 'node:path';
import { fileURLToPath } from 'node:url';

const HERE = dirname(fileURLToPath(import.meta.url));
const DEMOS_DIR = join(HERE, '..', 'components', 'demos');
const OUT_FILE = join(HERE, '..', 'components', 'preview-sources.generated.ts');

const files = readdirSync(DEMOS_DIR)
    .filter((f) => f.endsWith('.tsx'))
    .sort();

const entries = files.map((file) => {
    const name = file.replace(/\.tsx$/, '');
    const source = readFileSync(join(DEMOS_DIR, file), 'utf8');
    return [name, source];
});

const banner = `// AUTO-GENERATED by apps/docs/scripts/generate-preview-sources.mjs.
// Do not edit by hand. Re-run \`yarn docs predev\` or \`yarn docs prebuild\`
// to regenerate, or trigger via the workspace dev/build commands.

`;

const body = `export const previewSources = {\n${entries
    .map(([name, source]) => `    ${JSON.stringify(name)}: ${JSON.stringify(source)},`)
    .join('\n')}\n} as const;\n\nexport type PreviewSourceName = keyof typeof previewSources;\n`;

writeFileSync(OUT_FILE, banner + body);

console.log(`Generated ${relative(process.cwd(), OUT_FILE)} with ${entries.length} demos.`);
