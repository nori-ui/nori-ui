// Reads every demo under apps/docs/components/demos/*.tsx and writes a
// generated `preview-sources.generated.ts` map of `<demo-name>` →
// { raw, tokens, rootStyle }, where `raw` is the verbatim source and
// `tokens`/`rootStyle` are the shiki-highlighted structure ready for
// direct React rendering by the shared HighlightedCode component.
//
// Why structured tokens (not raw HTML): keeps shiki output entirely React-
// rendered, so React's escaping is the only sanitizer in the path and we
// never need raw-HTML injection.
//
// Why pre-build codegen instead of `?raw` queries: Next.js's swc-loader
// still transforms .tsx files even with a `resourceQuery` rule, so `?raw`
// returns transpiled HMR-wrapped output. Codegen sidesteps the loader
// pipeline and works under both webpack and turbopack identically.
//
// Run via the `predev` / `prebuild` package.json hooks.

import { readdirSync, readFileSync, writeFileSync } from 'node:fs';
import { dirname, join, relative } from 'node:path';
import { fileURLToPath } from 'node:url';
import { getSingletonHighlighter } from 'shiki';

const HERE = dirname(fileURLToPath(import.meta.url));
const DEMOS_DIR = join(HERE, '..', 'components', 'demos');
const OUT_FILE = join(HERE, '..', 'components', 'preview-sources.generated.ts');

const THEMES = { light: 'github-light', dark: 'github-dark' };

const parseStyleString = (s) => {
    const obj = {};
    if (!s) {
        return obj;
    }
    for (const decl of s.split(';')) {
        const i = decl.indexOf(':');
        if (i === -1) {
            continue;
        }
        const k = decl.slice(0, i).trim();
        const v = decl.slice(i + 1).trim();
        if (k && v) {
            obj[k] = v;
        }
    }
    return obj;
};

const highlighter = await getSingletonHighlighter({
    themes: Object.values(THEMES),
    langs: ['tsx'],
});

const files = readdirSync(DEMOS_DIR)
    .filter((f) => f.endsWith('.tsx'))
    .sort();

const entries = files.map((file) => {
    const name = file.replace(/\.tsx$/, '');
    const raw = readFileSync(join(DEMOS_DIR, file), 'utf8');
    const result = highlighter.codeToTokens(raw, {
        lang: 'tsx',
        themes: THEMES,
        defaultColor: false,
    });
    // Per-token `htmlStyle` is already an object (CSS variables); only the
    // `rootStyle` is a string in shiki 1.x and needs parsing.
    const tokens = result.tokens.map((line) =>
        line.map((tok) => ({ content: tok.content, style: tok.htmlStyle ?? {} }))
    );
    const rootStyle = parseStyleString(result.rootStyle);
    return { name, raw, tokens, rootStyle };
});

const banner = `// AUTO-GENERATED by apps/docs/scripts/generate-preview-sources.mjs.
// Do not edit by hand. Re-run via the workspace predev/prebuild hooks.
import type { Highlighted } from '@/lib/highlight';

`;

const body = `export type PreviewSource = Highlighted & { raw: string };

export const previewSources = {
${entries
    .map(
        (e) =>
            `    ${JSON.stringify(e.name)}: { raw: ${JSON.stringify(e.raw)}, tokens: ${JSON.stringify(e.tokens)}, rootStyle: ${JSON.stringify(e.rootStyle)} },`
    )
    .join('\n')}
} satisfies Record<string, PreviewSource>;

export type PreviewSourceName = keyof typeof previewSources;
`;

writeFileSync(OUT_FILE, banner + body);

console.log(`Generated ${relative(process.cwd(), OUT_FILE)} with ${entries.length} demos.`);
