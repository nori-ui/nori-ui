// Walks every component in packages/core/src/components/<Name>/<Name>.tsx
// (plus the Icon wrapper) and extracts a structured prop list using
// react-docgen-typescript. Output: apps/docs/components/props.generated.ts
// — consumed by the <PropsTable> MDX component.
//
// Source of truth is the component's TypeScript prop type plus its JSDoc
// comments. When you rename a prop, change a default, or add an
// `@deprecated` tag, the table updates on the next docs build — no MDX
// edits required.
//
// Run via the predev/prebuild package.json hooks alongside the other
// generators.

import { existsSync, readdirSync, writeFileSync } from 'node:fs';
import { dirname, join, relative } from 'node:path';
import { fileURLToPath } from 'node:url';
import docgen from 'react-docgen-typescript';

const HERE = dirname(fileURLToPath(import.meta.url));
const REPO_ROOT = join(HERE, '..', '..', '..');
const COMPONENTS_DIR = join(REPO_ROOT, 'packages', 'core', 'src', 'components');
const ICON_FILE = join(REPO_ROOT, 'packages', 'core', 'src', 'icons', 'icon.tsx');
const OUT_FILE = join(HERE, '..', 'components', 'props.generated.ts');
const TSCONFIG = join(REPO_ROOT, 'packages', 'core', 'tsconfig.json');

const componentFiles = [];
for (const dir of readdirSync(COMPONENTS_DIR)) {
    const entry = join(COMPONENTS_DIR, dir, `${dir}.tsx`);
    if (existsSync(entry)) componentFiles.push(entry);
}
componentFiles.push(ICON_FILE);

const parser = docgen.withCustomConfig(TSCONFIG, {
    savePropValueAsString: true,
    shouldExtractLiteralValuesFromEnum: true,
    shouldRemoveUndefinedFromOptional: true,
    propFilter: (prop) => {
        // Drop noise: HTML attribute spread from React.HTMLAttributes, RN
        // PressableProps that aren't part of the library's surface, etc.
        if (prop.parent) {
            const fileName = prop.parent.fileName ?? '';
            if (fileName.includes('node_modules')) return false;
            if (fileName.includes('@types/react')) return false;
            if (fileName.includes('react-native')) return false;
        }
        return true;
    },
});

const docs = parser.parse(componentFiles);

const props = {};
for (const c of docs) {
    const rows = Object.entries(c.props).map(([name, info]) => ({
        name,
        type: info.type?.name ?? 'unknown',
        defaultValue: info.defaultValue?.value ?? null,
        required: Boolean(info.required),
        description: info.description ?? '',
        deprecated: info.tags?.deprecated ?? null,
    }));
    rows.sort((a, b) => {
        if (a.required !== b.required) return a.required ? -1 : 1;
        return a.name.localeCompare(b.name);
    });
    props[c.displayName] = rows;
}

const banner = `// AUTO-GENERATED by apps/docs/scripts/generate-props.mjs.
// Do not edit by hand. Re-run via the workspace predev/prebuild hooks.

`;

const body = `export type PropRow = {
    name: string;
    type: string;
    defaultValue: string | null;
    required: boolean;
    description: string;
    deprecated: string | null;
};

export const componentProps = ${JSON.stringify(props, null, 4)} satisfies Record<string, PropRow[]>;

export type ComponentName = keyof typeof componentProps;
`;

writeFileSync(OUT_FILE, banner + body);

console.log(`Generated ${relative(process.cwd(), OUT_FILE)} with ${Object.keys(props).length} components.`);
